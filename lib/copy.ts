// Helpers
export function pickN<T>(arr: T[], n: number): T[] {
    const shuffled = [...arr].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, n);
}

export function pickOne<T>(arr: T[]): T {
    return arr[Math.floor(Math.random() * arr.length)];
}
